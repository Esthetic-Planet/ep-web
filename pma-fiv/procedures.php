            </div>
            <div class="col-md-10 col-sm-10 col-xs-12">
				<a href="https://www.esthetic-planet.com/pma-fiv/don-ovocyte.php" style="font-weight: 900;text-decoration: underline;color:white;"><h3>Don d'ovocyte</h3></a>
                Le don d'ovocyte représente un véritable espoir pour les couples qui ne peuvent pas avoir d'enfant, parce que les femmes n'ont naturellement pas d'ovocytes ou ses ovocytes présentent des anomalies. <br><br>
            </div>
        </div>
