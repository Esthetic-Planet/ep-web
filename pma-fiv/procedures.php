            <div class="col-md-10 col-sm-10 col-xs-12">
				<a href="https://www.esthetic-planet.com/pma-fiv/don-sperme.php" style="font-weight: 900;text-decoration: underline;color:white;"><h3>Don de sperme</h3></a>
                Le don de sperme est destiné à des couples dont les hommes sont infertiles. Lors de la sélection, l'équipe médicale fait en sorte que les caractéristiques physiques (groupe sanguin, couleur de peau, des yeux et des cheveux) du donneur correspondent à celles des parents. <br><br>
            </div>
