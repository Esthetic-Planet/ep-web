            <div class="col-md-10 col-sm-10 col-xs-12">
				<a href="https://www.esthetic-planet.com/pma-fiv/fiv.php" style="font-weight: 900;text-decoration: underline;color:white;"> <h3>FIV - Fécondation In Vitro</h3></a>
                La FIV (Fécondation in Vitro) consiste à féconder un ovule avec un spermatozoïde à l'extérieur de l'utérus, en laboratoire. Chaque ovule préalablement extrait est introduit d'un spermatozoïde afin de le féconder. <br><br>
            </div>
        </div>
