            </div>
            <div class="col-md-10 col-sm-10 col-xs-12">
               <a href="https://www.esthetic-planet.com/pma-fiv/icsi.php" style="font-weight: 900;text-decoration: underline;color:white;"> <h3>FIV ICSI - FIV assitée</h3></a>
                La procédure ICSI, complément de la FIV classique, consiste à inséminer un ovule en y micro-injectant un spermatozoïde. L'ICSI est recommandé au cas où l'on dispose d'un nombre très faible de spermatozoïdes dans le sperme ou si ceux-ci présentent une très mauvaise mobilité.<br><br>
            </div>
        </div>
