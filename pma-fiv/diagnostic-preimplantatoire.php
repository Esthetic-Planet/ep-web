            </div>
        </div>

        <div class="row">
            <div class="col-md-12 col-sm-12 col-xs-12">
                <p>Lors de la consultation, un examen est effectué pour déterminer les attentes. Un traitement sera prescrit pour stimuler l'ovulation. La stimulation ovarienne permet d’administrer à la femme un traitement hormonal pour augmenter le nombre de follicules produits par les ovaires et contrôler le déroulement de l’ovulation. La stimulation nécessite des injections sous-cutanées et 3 à 4 visites cliniques pour les tests sanguins et les échographies de contrôle.</p>

            </div>
        </div>

