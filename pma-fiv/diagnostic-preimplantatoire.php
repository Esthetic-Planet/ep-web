    <h2 class="text-center" style="font-size:29px;padding-bottom:10px;color:white;margin-bottom:20px;">POUR QUI LE DIAGNOSTIC PRÉIMPLANTATOIRE EST-IL RECOMMANDÉ ?
</h2>
        <div class="row">
            <div class="col-md-12 col-sm-12 col-xs-12">
                <p>En PMA, le diagnostic préimplantatoire (DPI) permet l'analyse génétique des embryons obtenus après la biopsie. Elle est pour but d'éviter une interruption de grossesse en détectant les embryons porteurs d'une anomalie monogénique ou chromosomique. A l'aide du DPI, on peut détecter chez les embryons les maladies courantes telles que le nanisme diastrophique, la maladie de Kugelberg-Welander, la mucoviscidose, les chromosomes surnuméraires, etc.
Il est utile pour les couples ayant subi de nombreux échecs et fausse couche, car cela peut être causé par des anomalies chromosomiques et/ou une maladie ou anomalie génétique.
</p>
            <p>Les femmes au dessus de 40 ans  sont également concernées car les risques d’anomalies chromosomiques augmentent significativement à partir de 40 ans.
Il faut savoir qu’en France, cette technique est accordée après étude du dossier, aux couples ayant une forte probabilité de transmettre une maladie héréditaire grave, reconnue par des médecins, comme incurable au moment du diagnostic. La validation des dossiers est prise par le centre de DPI et prennent du temps.</p>
            <p>En Espagne ou en Georgie le diagnostic préimplantatoire est accessible à tout le monde, sans conditions médicales.</p>
            </div>
        </div>


        <h2 class="text-center" style="font-size:29px;padding-bottom:10px;color:white;margin-bottom:20px;margin-top:50px;">Comment se passe le diagnostic préimplantatoire en Espagne ?</h2>

        <div class="row">
            <div class="col-md-12 col-sm-12 col-xs-12">
                <p>Le DPI est pratiqué chez l'embryon des couples ayant recours à une fécondation in vitro uniquement.</p>
            <p>Le séjour se déroule de la même manière qu’une FIV classique qui est réparti en 2 séjours. Le DPI est réalisé lors du deuxième séjour.</p>
            </div>
        </div>

